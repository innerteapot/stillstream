<h1>Privacy</h1>

<h2>Summary</h2>
<p>
	StillStream is totally dedicated to the preservation of privacy for its listeners, its
	artists, and its staff. This privacy statement discloses the types of information we
	gather, how we use it, and how to correct or change it.
</p>
<p>
	Our privacy policy can be summarized as follows:
</p>
<ol>
	<li>
		Browsing our web site is effectively anonymous.  The only information we keep is
		standard web log stuff like IP address and time of day.
	</li>
	<li>
		Listening to our stream is also effectively anonymous.  The information kept is
		the same as for the web server; no personally identifying information other than
		IP address.
	</li>
	<li>
		If you contact us or otherwise engage with us such that we do obtain individually
		identifying information, our privacy policy states we will not share it with anyone
		unless legally compelled to.
	</li>
</ol>
<p>
	For specific details about our policy, please read below.
</p>


<h2>Information Gathered</h2>
<p>
	StillStream does not gather any individually identifying
	information about its users, with the following sole exceptions:
</p>
<ol>
	<li>
		IP addresses, domain names, and other technical information commonly gathered in the
		web logs of any standard web server.
	</li>
	<li>
		Email addresses of those users who voluntarily subscribe to the StillStream newsletter.
	</li>
	<li>
		Email address and name of those users who <a title="How To Reach Us" href="contact.php">contact us</a>
		via the contact form.
	</li>
	<li>
		Several pieces of information about artists and tracks that we play on our station.
	</li>
</ol>
<p>
	If you have contacted us, your name and email address are kept only as long as is
	necessary for us to reply to your note and conclude the discussion with you.
	Additionally, while we keep this information, it is kept according to
	our confidentiality terms listed below.
</p>
<p>
	If you have subscribed to our newsletter and would like to unsubscribe,
	simply <a title="How To Reach Us" href="contact.php">contact us</a> and we will remove your email
	address promptly and permanently.
</p>
<p>
	If you are an artist or a copyright holder who has given us permission to play
	your music, your information is kept on file indefinitely for legal documentation
	purposes. This information is also kept according to our confidentiality terms
	listed below.
<p>
	Technical information stored in the form of web server logs may be kept by us
	indefinitely, for the purpose of analyzing traffic patterns of our web site.
	This information is also kept according to our confidentiality terms
	listed below.
</p>



<h2>Cookies</h2>
<p>
	StillStream may employ the use of so-called 'session cookies' in order to facilitate
	the delivery of its content and services.  These cookies do not contain any
	individually identifying information, and are automatically
	destroyed when the user closes their browser.
</p>



<h2>Confidentiality</h2>
<p>
	For any individually identifying information that StillStream keeps about its users, the
	following confidentiality policy applies.
</p>
<p>
	Put simply, StillStream will not share this information with any third party, for any
	reason, unless we are legally compelled to do so.  In other words, we will keep your
	information strictly confidential between you and ourselves, we will store it in a
	reasonably secure data storage environment, and we will not give it to any third party,
	including governmental or law enforcement entities, unless we are required to do so by law.
</p>
<p>
	We value the relationship with our users, staff, and artists, and we will do everything
	within our power to ensure your information is kept securely and confidentially.
</p>



<h2>Privacy Of Children</h2>
<p>
	StillStream is not directed to children under the age of 13. We operate our station
	in compliance with the Children's Online Privacy Protection Act and will not
	knowingly collect or use personally identifiable information from anyone under
	13 years of age.
</p>



