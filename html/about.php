<h1>About StillStream</h1>

<h2>Summary</h2>
<p>
	StillStream is an ambient, non-commercial, zero-revenue independent net radio station based in the United States.  Our mission is
	to make ambient artists successful.  We play strictly by the rules when it comes to music licensing.  There are numerous people
	to whom we are grateful for generous support.  We have been
	broadcasting since 2005 and hope to keep broadcasting for many years to come.
</p>

<h2>Our Story</h2>
<p>
	The original idea for StillStream came from Ian Clay of
	<a title="Parasme Records" href="http://parasme.com" target="_blank">Parasme Records</a>, who felt
	that the ambient genre needed more radio airplay options for artists.  He also felt
	that the world needed a dedicated net radio station that would both promote
	the ambient genre as well as bring together ambient artists and fans into a single
	supportive community. Inspired by this idea, we
	set about to create:
</p>
<ul class="bullets">
	<li class="bullets">An independent, non-commercial net radio station that ran no ads or other obnoxious content</li>
	<li class="bullets">A station that would play quality ambient music by artists at any level of fame or commercial success.</li>
	<li class="bullets">A place where fans could meet their favorite artists, and where artists could actually meet the people who dig their music.</li>
	<li class="bullets">A station that focused on live music performances and live radio programs as much as possible.</li>
	<li class="bullets">A community of artists and fans who would attend live broadcasts, both by listening and by participating in live chat.</li>
	<li class="bullets">A vehicle for expanding the fan base for the ambient genre as a whole.</li>
	<li class="bullets">A station that could be sustainably kept on the air despite having absolutely no revenue, without sending the owner to the poor house.</li>
</ul>
<p>
	After several weeks of work, StillStream first began broadcasting on March 1, 2005 and has been on the air non-stop ever since.
	During this time, we grew from having just a couple of listeners on an occasional basis to being a full fledged radio station
	with many listeners from all over the globe tuned in at all hours of the day and night.  Our <a title="Artists Listing" href="artists.php">library</a> grew from having
	just a few dozen artists to being one of the largest repositories of quality independent ambient music on the net.
	We even got to the #1 spot on Google for the search phrase "<a title="Search Google For Ambient Radio" href="http://www.google.com/search?q=ambient+radio" target="_blank">ambient radio</a>".
</p>
<p>
	With no false modesty, we think it is now safe to say that StillStream is one of the finest ambient net radio
	stations around.  Though we are by no means the only great ambient net radio station,
	we are the only ambient net radio station that we know of that focuses
	on live music and live programs.  We are very proud of StillStream and look forward to many years of continued
	service to the ambient community.  We hope our listeners find StillStream to be as fulfilling as we do.
</p>
<p>
	<b>Special Note:</b> In August of 2011, ownership of StillStream was transferred from Darrell Burgan to Howard Moscovitz of 
	<a title-"Electro-Music.com" href="http://electro-music.com">Electro-Music.com</a>.
	StillStream will continue to offer an excellent stream of ambient music as well as our live programming, but with the
	added benefit of being part of the even larger and more dynamic community of artists that is <a title-"Electro-Music.com" href="http://electro-music.com">Electro-Music.com</a>. Though
	Darrell is no longer associated with the ownership or maintenance of StillStream.com, we are very excited about the future
	of StillStream as we become part of the <a title-"Electro-Music.com" href="http://electro-music.com">Electro-Music.com</a> neighborhood.
</p>

<h2>Our Mission</h2>
<p>
	We measure StillStream's success as follows:
</p>
<ul class="bullets">
	<li class="bullets">Are we providing significant promotional value to ambient artists?</li>
	<li class="bullets">Are we expanding the ambient music listener base in general?</li>
	<li class="bullets">Are we creating excitement amongst ambient music fans?</li>
	<li class="bullets">Are we introducing listeners to new artists and releases that they might not otherwise hear?</li>
	<li class="bullets">Are we giving ambient artists a much-needed venue in which to perform live for their fans?</li>
	<li class="bullets">Are we helping to keep ambient music as a genre "on the map"?</li>
</ul>
<p>
	If we can answer yes to all of these questions, then we feel we are living up to our goals.  If not,
	well then it means we need to continue to improve our service, something we are constantly striving to do.
</p>
<p>
	It is important to note that our mission does NOT include making money, making a profit, or even covering
	our own costs.  We are intentionally and unapologetically non-commercial because this gives us the freedom
	to work towards our goals without any external entanglements.
</p>


<h2>The Licensing Thing</h2>
<p>
	When it comes to licensing music, it is our belief that net radio stations come in just two flavors:  those that play by the rules, and those that
	do not. StillStream plays strictly by the rules, and in fact goes to great lengths to ensure
	we are doing so.  We strongly support the rights of ambient artists, including their right to choose how to market their music,
	and how much (if anything) to charge for their music.
</p>
<p>
	But let's be clear: StillStream does not pay royalties of any kind for airplay. Put simply, as a non-commercial station with no
	revenue stream, it is out of our reach cost-wise.
</p>
<p>
	How, then, can we broadcast all this music legally? Simple. We just <a title="Submissions" href="submissions.php">ask for the artist's permission</a>.
</p>
<p>
	After much research and a couple of different models for documenting permission, StillStream has settled upon a simple set of rules:
</p>
<ul class="bullets">
	<li class="bullets">Music released under <a title="Creative Commons" href="http://creativecommons.org" target="_blank">Creative Commons</a> is automatically free for StillStream to use, owing to our non-commercial status.</li>
	<li class="bullets">Non Creative Commons music can also be played on StillStream, but only if the artist/rightsholder signs our online royalty waiver form.</li>
</ul>
<p>
	We totally respect artists who choose not to grant us a royalty waiver.  We believe very strongly that artists
	should have the choice of how to bring their music to the marketplace and to their audiences, and can understand if
	an artist would rather not sign such an agreement.  After all, we are
	all artists ourselves.  If you do choose to grant us a royalty waiver, however, we sincerely thank you and promise to do
	everything we can to make it worth your effort.
</p>


